print('Verificar de Números')
n1 = int(input('\nDigite seu primeiro número: '))
n2 = int(input('\nDigite seu segundo número: '))
if n1 > n2:
    print('\nO primeiro número é maior que o segundo!')
elif n1 < n2:
    print('\nO primeiro número é menor que o segundo!')
else:
    print('\nOs dois números são iguais!')
    
